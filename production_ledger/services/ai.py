def create_ai_artifact(
    episode: Episode,
    artifact_type: str,
    prompt: str,
    output: str,
    context_refs: dict,
    user,
    config: Optional[dict] = None,
) -> AIArtifact:
    """
    Create an AIArtifact with full provenance.
    
    Args:
        episode: The episode this artifact belongs to
        artifact_type: Type from ArtifactType choices
        prompt: The prompt sent to the AI
        output: The AI-generated output
        context_refs: References to input data (transcript_id, clip_ids, etc.)
        user: The user creating this artifact
        config: Optional AI config dict
    
    Returns:
        AIArtifact instance (saved)
    """
    if config is None:
        config = get_ai_config()
    
    artifact = AIArtifact.objects.create(
        episode=episode,
        organization_uuid=episode.organization_uuid,
        artifact_type=artifact_type,
        input_prompt=prompt,
        input_context_refs=context_refs,
        output_text=output,
        provider=config.get('provider', 'mock'),
        model=config.get('model', 'unknown'),
        params={
            'temperature': config.get('temperature', 0.7),
            'max_tokens': config.get('max_tokens', 2000),
        },
        approval_status=ApprovalStatus.PENDING,
        transparency_summary=f"Generated by {config.get('provider', 'mock')} using {config.get('model', 'unknown')}",
        created_by=user,
    )
    
    return artifact


