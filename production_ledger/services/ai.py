def generate_questions(
    episode: Episode,
    topic_prompt: str,
    user,
    transcript: Optional[Transcript] = None,
) -> AIArtifact:
    """
    Generate interview questions for an episode.
    
    Args:
        episode: The episode to generate questions for
        topic_prompt: Topic/focus for the questions
        user: The user requesting generation
        transcript: Optional transcript to use as context
    
    Returns:
        AIArtifact with generated questions
    """
    config = get_ai_config()
    provider = get_provider(config)
    
    # Build the prompt
    prompt = f"""Generate thoughtful interview questions for a podcast episode.

Episode: {episode.title}
Show: {episode.show.name}
Topic Focus: {topic_prompt}
Episode Type: {episode.get_episode_type_display()}
Target Duration: {episode.target_minutes} minutes
"""
    
    # Add guest context if available
    guests = episode.episode_guests.select_related('guest').all()
    if guests:
        prompt += "\n\nGuests:\n"
        for eg in guests:
            prompt += f"- {eg.guest.name}"
            if eg.guest.title:
                prompt += f", {eg.guest.title}"
            if eg.guest.org:
                prompt += f" at {eg.guest.org}"
            prompt += "\n"
            if eg.key_topics:
                prompt += f"  Key topics: {eg.key_topics}\n"
            if eg.no_go_topics:
                prompt += f"  Avoid: {eg.no_go_topics}\n"
    
    # Build context from transcript
    context = ""
    context_refs = {}
    
    if transcript:
        context = transcript.raw_text[:5000]  # Limit context size
        context_refs['transcript_id'] = str(transcript.id)
    
    prompt += f"\n\nPlease generate {10 if episode.target_minutes >= 45 else 5} questions that flow naturally, ranging from opening warmup to deeper exploration."
    
    # Generate
    output = provider.generate(prompt, context)
    
    # Create artifact
    return create_ai_artifact(
        episode=episode,
        artifact_type=ArtifactType.QUESTIONS,
        prompt=prompt,
        output=output,
        context_refs=context_refs,
        user=user,
        config=config,
    )


def generate_show_notes(
    episode: Episode,
    user,
    transcript: Optional[Transcript] = None,
    clips: Optional[list] = None,
) -> AIArtifact:
    """
    Generate show notes for an episode.
    
    Args:
        episode: The episode to generate notes for
        user: The user requesting generation
        transcript: Optional transcript to use as context
        clips: Optional list of ClipMoment objects
    
    Returns:
        AIArtifact with generated show notes
    """
    config = get_ai_config()
    provider = get_provider(config)
    
    # Build the prompt
    prompt = f"""Generate comprehensive show notes for a podcast episode.

Episode: {episode.title}
Show: {episode.show.name}
Episode Type: {episode.get_episode_type_display()}
"""
    
    # Add segments context
    segments = episode.segments.all().order_by('order')
    if segments:
        prompt += "\n\nSegments (Run of Show):\n"
        for seg in segments:
            prompt += f"- {seg.title} ({seg.timebox_minutes} min): {seg.purpose}\n"
    
    # Add guest context
    guests = episode.episode_guests.select_related('guest').all()
    if guests:
        prompt += "\n\nGuests:\n"
        for eg in guests:
            prompt += f"- {eg.guest.name}"
            if eg.guest.title and eg.guest.org:
                prompt += f", {eg.guest.title} at {eg.guest.org}"
            prompt += "\n"
    
    # Build context
    context = ""
    context_refs = {}
    
    if transcript:
        context = transcript.raw_text[:8000]
        context_refs['transcript_id'] = str(transcript.id)
    
    if clips:
        context_refs['clip_ids'] = [str(c.id) for c in clips]
        prompt += "\n\nHighlighted Moments:\n"
        for clip in clips[:10]:  # Limit to 10 clips
            prompt += f"- [{clip.start_formatted}] {clip.title}: {clip.hook}\n"
    
    prompt += "\n\nGenerate show notes including: summary, key topics, takeaways, resources mentioned, and timestamps."
    
    # Generate
    output = provider.generate(prompt, context)
    
    # Create artifact
    return create_ai_artifact(
        episode=episode,
        artifact_type=ArtifactType.SHOW_NOTES,
        prompt=prompt,
        output=output,
        context_refs=context_refs,
        user=user,
        config=config,
    )


def generate_social_posts(
    episode: Episode,
    user,
    show_notes_draft: Optional[str] = None,
) -> AIArtifact:
    """
    Generate social media posts for an episode.
    
    Args:
        episode: The episode to generate posts for
        user: The user requesting generation
        show_notes_draft: Optional show notes to use as context
    
    Returns:
        AIArtifact with generated social posts
    """
    config = get_ai_config()
    provider = get_provider(config)
    
    # Build the prompt
    prompt = f"""Generate social media posts to promote a podcast episode.

Episode: {episode.title}
Show: {episode.show.name}

Please generate posts for:
1. LinkedIn (professional, 1-2 paragraphs)
2. Twitter/X (thread format, 5-7 tweets)
3. Instagram (caption with emoji, call to action)
"""
    
    # Add guest context for tagging
    guests = episode.episode_guests.select_related('guest').all()
    if guests:
        prompt += "\n\nGuests to mention:\n"
        for eg in guests:
            g = eg.guest
            prompt += f"- {g.name}"
            if g.links and isinstance(g.links, dict):
                if g.links.get('twitter'):
                    prompt += f" (@{g.links['twitter']})"
                if g.links.get('linkedin'):
                    prompt += f" [LinkedIn: {g.links['linkedin']}]"
            prompt += "\n"
    
    # Build context
    context = show_notes_draft[:3000] if show_notes_draft else ""
    context_refs = {}
    
    if show_notes_draft:
        context_refs['show_notes_source'] = 'draft'
    
    # Generate
    output = provider.generate(prompt, context)
    
    # Create artifact
    return create_ai_artifact(
        episode=episode,
        artifact_type=ArtifactType.SOCIAL_POSTS,
        prompt=prompt,
        output=output,
        context_refs=context_refs,
        user=user,
        config=config,
    )


